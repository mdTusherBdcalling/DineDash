import 'package:flutter/material.dart';

class AppPadding {
  static const double defaultPadding = 16.0;
  static const EdgeInsets all = EdgeInsets.all(defaultPadding);
  static const EdgeInsets horizontal = EdgeInsets.symmetric(horizontal: defaultPadding);
  static const EdgeInsets vertical = EdgeInsets.symmetric(vertical: defaultPadding);
  static const EdgeInsets top = EdgeInsets.only(top: defaultPadding);
  static const EdgeInsets bottom = EdgeInsets.only(bottom: defaultPadding);
  static const EdgeInsets left = EdgeInsets.only(left: defaultPadding);
  static const EdgeInsets right = EdgeInsets.only(right: defaultPadding);
}
